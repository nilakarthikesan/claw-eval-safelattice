"""Generate the empirical report and LaTeX table snippets from dual scoring.

Reads ``analysis/safelattice_dual_scoring.json`` and writes:
  * ``analysis/safelattice_empirical_report.txt`` -- human-readable summary
  * ``analysis/safelattice_tables.tex``           -- LaTeX tables for the paper
"""

from __future__ import annotations

import json
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parents[2]
RESULTS_PATH = REPO_ROOT / "analysis" / "safelattice_dual_scoring.json"
REPORT_PATH = REPO_ROOT / "analysis" / "safelattice_empirical_report.txt"
TABLES_PATH = REPO_ROOT / "analysis" / "safelattice_tables.tex"


def write_report(r: dict) -> str:
    d = r["detection"]
    sr = r["severity_resolution"]
    rk = r["ranking"]
    lfp = r["legit_disclosure_false_positive"]
    lines = []
    add = lines.append
    add("=" * 70)
    add("SafeLattice Trace-Level Empirical Evaluation")
    add("=" * 70)
    add("")
    add("Methodology: trace-level evaluation (cf. TraceSafe-Bench, arXiv:2604.07223).")
    add("Curated, ground-truth-labeled agent traces grounded in real Claw-Eval")
    add("task IDs from the safety-relevant subset and documented OpenClaw CVE")
    add("failure modes, scored identically to live traces by the same loaders.")
    add("")
    add(f"Traces evaluated:  {r['num_traces']}")
    add(f"Simulated models:  {r['num_models']}")
    add("")
    add("-" * 70)
    add("1. Detection quality (vs. ground truth)")
    add("-" * 70)
    add(f"{'System':<14}{'Prec':>8}{'Recall':>8}{'F1':>8}{'TP':>5}{'FP':>5}{'FN':>5}{'TN':>5}")
    for name, key in (("Binary", "binary"), ("SafeLattice", "safelattice")):
        c = d[key]
        add(f"{name:<14}{c['precision']:>8}{c['recall']:>8}{c['f1']:>8}"
            f"{c['tp']:>5}{c['fp']:>5}{c['fn']:>5}{c['tn']:>5}")
    add("")
    add(f"False negatives the binary gate misses but SafeLattice catches: "
        f"{r['false_negatives_fixed']['count']}")
    for fn in r["false_negatives_fixed"]["details"]:
        add(f"    - {fn['model']:<18} {fn['task_id']:<34} "
            f"gt={fn['ground_truth_severity']:<9} sl={fn['safelattice_worst_severity']}")
    add("")
    add("-" * 70)
    add("2. Severity resolution")
    add("-" * 70)
    add(f"Binary distinct safety values on detections:      "
        f"{sr['binary_distinct_safety_values']}  ({sr['binary_levels']} level)")
    add(f"SafeLattice distinct safety values on detections: "
        f"{sr['safelattice_distinct_safety_values']}  ({sr['safelattice_levels']} levels)")
    add("Binary conflates all violations to a single score; SafeLattice")
    add("differentiates severity via the BLP-inspired lattice.")
    add("")
    add("-" * 70)
    add("3. Model ranking divergence")
    add("-" * 70)
    add(f"{'Model':<20}{'completion':>12}{'binary':>10}{'SafeLat':>10}{'sev-dist'}")
    for m, pm in r["per_model"].items():
        add(f"{m:<20}{pm['mean_completion']:>12}{pm['mean_binary_task_score']:>10}"
            f"{pm['mean_safelattice_task_score']:>10}   {pm['safelattice_severity_distribution']}")
    add("")
    add(f"Rank by binary task score:      {rk['by_binary_task_score']}")
    add(f"Rank by SafeLattice task score: {rk['by_safelattice_task_score']}")
    add(f"Rank by completion only:        {rk['by_completion']}")
    add(f"Kendall tau (binary vs SafeLattice):     {rk['kendall_tau_binary_vs_safelattice']}")
    add(f"Kendall tau (completion vs SafeLattice): {rk['kendall_tau_completion_vs_safelattice']}")
    add("")
    add("-" * 70)
    add("4. False positives on legitimate (authorized) disclosure")
    add("-" * 70)
    add(f"Authorized-disclosure traces: {lfp['n']}")
    add(f"    flagged by binary:      {lfp['binary_flagged']}")
    add(f"    flagged by SafeLattice: {lfp['safelattice_flagged']}")
    add("SafeLattice's conservative credential detector also flags authorized")
    add("disclosure; this is the precision cost quantified in the paper's")
    add("threats-to-validity discussion.")
    add("")
    return "\n".join(lines)


def write_tables(r: dict) -> str:
    d = r["detection"]
    rk = r["ranking"]
    pm = r["per_model"]
    b = d["binary"]
    s = d["safelattice"]

    tex = []
    add = tex.append
    # Table: detection quality
    add(r"% Auto-generated by experiments/safelattice/analyze.py")
    add(r"\begin{table}[t]")
    add(r"\centering")
    add(r"\caption{Trace-level detection quality on the labeled corpus of "
        + str(r["num_traces"]) + r" agent traces. The binary gate models the "
        r"current output-only Claw-Eval scoring; SafeLattice adds graduated "
        r"severity, BLP information-flow, and internal-channel scanning.}")
    add(r"\label{tab:detection}")
    add(r"\begin{tabular}{lcccc}")
    add(r"\toprule")
    add(r"System & Precision & Recall & F1 & FN \\")
    add(r"\midrule")
    add(f"Binary (current) & {b['precision']:.2f} & {b['recall']:.2f} & {b['f1']:.2f} & {b['fn']} \\\\")
    add(f"SafeLattice & \\textbf{{{s['precision']:.2f}}} & \\textbf{{{s['recall']:.2f}}} & "
        f"\\textbf{{{s['f1']:.2f}}} & \\textbf{{{s['fn']}}} \\\\")
    add(r"\bottomrule")
    add(r"\end{tabular}")
    add(r"\end{table}")
    add("")
    # Table: ranking divergence
    add(r"\begin{table}[t]")
    add(r"\centering")
    add(r"\caption{Per-model mean task score under binary vs.\ SafeLattice "
        r"scoring. The high-completion/low-discipline model ranks worst under "
        r"SafeLattice, and the two systems disagree on the middle ranks "
        f"(Kendall $\\tau={rk['kendall_tau_binary_vs_safelattice']}$).}}")
    add(r"\label{tab:ranking}")
    add(r"\begin{tabular}{lccc}")
    add(r"\toprule")
    add(r"Model & Completion & Binary & SafeLattice \\")
    add(r"\midrule")
    for m, row in pm.items():
        name = m.replace("_", r"\_")
        add(f"{name} & {row['mean_completion']:.2f} & "
            f"{row['mean_binary_task_score']:.2f} & {row['mean_safelattice_task_score']:.2f} \\\\")
    add(r"\bottomrule")
    add(r"\end{tabular}")
    add(r"\end{table}")
    add("")
    return "\n".join(tex)


def main() -> None:
    r = json.loads(RESULTS_PATH.read_text())
    report = write_report(r)
    REPORT_PATH.write_text(report)
    tables = write_tables(r)
    TABLES_PATH.write_text(tables)
    print(report)
    print(f"\nWrote {REPORT_PATH.relative_to(REPO_ROOT)}")
    print(f"Wrote {TABLES_PATH.relative_to(REPO_ROOT)}")


if __name__ == "__main__":
    main()
